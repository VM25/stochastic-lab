#include <cstdint>
#include <vector>

namespace diffusionworks {

/// Parameters for EXP-11: Heston calibration recovery.
///
/// A synthetic surface is generated from `true_parameters`, then calibrated from every
/// guess in `initial_guesses` -- none of which may be the truth. The catalog names
/// "the true parameters as the only initial guess" and "synthetic recovery omitted" as
/// failure conditions, so at least two guesses are required and at least one must sit
/// away from the truth; the recovery verdict is read from the best of those blind
/// starts, never from a start that began at the answer.
struct CalibrationRecoveryConfig {
    double spot{100.0};
    double rate{0.02};
    double dividend_yield{0.0};

    std::vector<double> strikes{80.0, 90.0, 100.0, 110.0, 120.0};
    std::vector<double> maturities{0.25, 0.5, 1.0, 2.0};

    /// The parameters the surface is generated from -- the answer the calibration is
    /// meant to recover without being told it.
    HestonParameters true_parameters{.initial_variance = 0.04,
                                     .mean_reversion = 1.5,
                                     .long_run_variance = 0.05,
                                     .vol_of_variance = 0.4,
                                     .correlation = -0.6};

    /// Diverse starting points, none the truth. The defaults span the box.
    std::vector<HestonParameters> initial_guesses{{.initial_variance = 0.03,
                                                   .mean_reversion = 1.0,
                                                   .long_run_variance = 0.06,
                                                   .vol_of_variance = 0.5,
                                                   .correlation = -0.5},
                                                  {.initial_variance = 0.09,
                                                   .mean_reversion = 0.8,
                                                   .long_run_variance = 0.09,
                                                   .vol_of_variance = 0.2,
                                                   .correlation = -0.85},
                                                  {.initial_variance = 0.02,
                                                   .mean_reversion = 2.5,
                                                   .long_run_variance = 0.03,
                                                   .vol_of_variance = 0.7,
                                                   .correlation = -0.3},
                                                  {.initial_variance = 0.06,
                                                   .mean_reversion = 3.0,
                                                   .long_run_variance = 0.04,
                                                   .vol_of_variance = 0.9,
                                                   .correlation = 0.0}};

    CalibrationObjectiveType objective{CalibrationObjectiveType::ImpliedVolatility};

    std::int64_t quadrature_nodes{256};
    int max_iterations{2000};

    /// The normalised parameter distance below which recovery counts as accurate, and
    /// the implied-volatility RMSE below which the surface fit counts as accurate.
    double recovery_tolerance{0.05};
    double fit_tolerance_iv_rmse{1.0e-3};

    /// A guess within this normalised distance of the truth is treated as "the truth"
    /// and excluded from the recovery verdict.
    double truth_guess_epsilon{0.05};
};

/// EXP-11: can calibration recover parameters from a synthetic surface generated by
/// known parameters?
///
/// Reports, and judges separately, the four things the catalog keeps apart: the
/// objective value, the surface fit (implied-volatility and price RMSE), the parameter
/// recovery (distance of the blind best fit from the truth), and identifiability
/// (materially different parameter vectors that fit about as well, preserved). Success
/// is never read from objective reduction alone, and the recovery is measured from a
/// start that did not begin at the answer.
[[nodiscard]] Result<ExperimentRecord>
run_heston_calibration_recovery(const CalibrationRecoveryConfig& config);

}  // namespace diffusionworks
