Result<ExperimentRecord> run_heston_calibration_recovery(const CalibrationRecoveryConfig& config) {
    const auto start_time = std::chrono::steady_clock::now();

    ExperimentRecord record;
    record.id = "EXP-11";
    record.name = "Heston Calibration Recovery";
    record.question =
        "Can calibration recover parameters from a synthetic surface generated by known "
        "parameters?";
    record.reproduction_command =
        "diffusionworks experiment --id EXP-11 --config configs/experiment/calibration.json";

    const HestonParameterBounds bounds = HestonParameterBounds::defaults();
    const HestonParameters& truth = config.true_parameters;

    if (config.initial_guesses.size() < 2) {
        return Result<ExperimentRecord>::failure(
            ErrorCode::InvalidArgument,
            "recovery needs at least two initial guesses; a single guess -- especially the true "
            "parameters -- is a failure condition the catalog names",
            "EXP-11");
    }

    // Which guesses are "blind": far enough from the truth that recovering the truth
    // from them is genuine, not a start that began at the answer.
    std::vector<bool> is_blind(config.initial_guesses.size(), false);
    std::size_t blind_count = 0;
    for (std::size_t i = 0; i < config.initial_guesses.size(); ++i) {
        const double distance =
            normalized_parameter_distance(config.initial_guesses[i], truth, bounds);
        is_blind[i] = distance > config.truth_guess_epsilon;
        if (is_blind[i]) {
            ++blind_count;
        }
    }
    if (blind_count == 0) {
        return Result<ExperimentRecord>::failure(
            ErrorCode::InvalidArgument,
            "every initial guess coincides with the true parameters; the catalog names calibrating "
            "only from the truth as a failure condition, so the recovery could not be demonstrated "
            "blind",
            "EXP-11");
    }

    // Generate the synthetic surface from the truth.
    SyntheticSurfaceSpec spec;
    spec.spot = config.spot;
    spec.rate = config.rate;
    spec.dividend_yield = config.dividend_yield;
    spec.strikes = config.strikes;
    spec.maturities = config.maturities;
    spec.parameters = truth;
    const auto surface = generate_heston_surface(spec);
    if (!surface) {
        return Result<ExperimentRecord>::failure(surface.error());
    }

    // Calibrate from every guess.
    CalibrationConfig calibration;
    calibration.bounds = bounds;
    calibration.initial_guesses = config.initial_guesses;
    calibration.objective = config.objective;
    calibration.pricing = HestonAnalyticConfig{.quadrature_nodes = config.quadrature_nodes,
                                               .convergence_tolerance = 1.0e-5};
    calibration.optimizer.max_iterations = config.max_iterations;
    const auto calibrated = calibrate_heston(surface.value(), calibration);
    if (!calibrated) {
        return Result<ExperimentRecord>::failure(calibrated.error());
    }
    const CalibrationResult& result = calibrated.value();

    // The best blind start: the lowest-objective fit among the guesses that did not
    // begin at the truth. This, not the global best, is what the recovery verdict is
    // read from.
    std::optional<std::size_t> best_blind;
    std::uint64_t started = 0;
    std::uint64_t converged = 0;
    for (std::size_t i = 0; i < result.starts.size(); ++i) {
        const CalibrationStart& s = result.starts[i];
        if (!s.started) {
            continue;
        }
        ++started;
        if (s.status == NelderMeadStatus::Converged) {
            ++converged;
        }
        if (is_blind[i] && (!best_blind.has_value() ||
                            s.objective_value < result.starts[*best_blind].objective_value)) {
            best_blind = i;
        }
    }

    record.configuration = nlohmann::json{{"spot", config.spot},
                                          {"rate", config.rate},
                                          {"dividend_yield", config.dividend_yield},
                                          {"strikes", config.strikes},
                                          {"maturities", config.maturities},
                                          {"true_parameters", to_json(truth)},
                                          {"objective", to_string(config.objective)},
                                          {"quadrature_nodes", config.quadrature_nodes},
                                          {"max_iterations", config.max_iterations},
                                          {"recovery_tolerance", config.recovery_tolerance},
                                          {"fit_tolerance_iv_rmse", config.fit_tolerance_iv_rmse},
                                          {"truth_guess_epsilon", config.truth_guess_epsilon},
                                          {"initial_guess_count", config.initial_guesses.size()},
                                          {"blind_guess_count", blind_count}};

    record.table.headers = {"start",
                            "blind",
                            "status",
                            "objective",
                            "iv_rmse",
                            "price_rmse",
                            "distance_to_truth",
                            "evaluations",
                            "v0",
                            "kappa",
                            "theta",
                            "xi",
                            "rho"};
    nlohmann::json start_records = nlohmann::json::array();
    for (std::size_t i = 0; i < result.starts.size(); ++i) {
        const CalibrationStart& s = result.starts[i];
        const double distance =
            s.started ? normalized_parameter_distance(s.calibrated, truth, bounds) : 0.0;
        record.table.rows.push_back(
            {std::to_string(i),
             is_blind[i] ? "yes" : "no",
             s.started ? std::string(to_string(s.status)) : std::string("not_started"),
             s.started ? number(s.objective_value) : std::string("-"),
             s.started ? number(s.implied_vol_rmse) : std::string("-"),
             s.started ? number(s.price_rmse) : std::string("-"),
             s.started ? number(distance) : std::string("-"),
             s.started ? std::to_string(s.function_evaluations) : std::string("-"),
             number(s.calibrated.initial_variance),
             number(s.calibrated.mean_reversion),
             number(s.calibrated.long_run_variance),
             number(s.calibrated.vol_of_variance),
             number(s.calibrated.correlation)});
        start_records.push_back(nlohmann::json{{"index", i},
                                               {"blind", is_blind[i]},
                                               {"started", s.started},
                                               {"note", s.note},
                                               {"status", to_string(s.status)},
                                               {"objective", s.objective_value},
                                               {"implied_vol_rmse", s.implied_vol_rmse},
                                               {"price_rmse", s.price_rmse},
                                               {"quotes_failed", s.quotes_failed},
                                               {"function_evaluations", s.function_evaluations},
                                               {"distance_to_truth", distance},
                                               {"initial", to_json(s.initial)},
                                               {"calibrated", to_json(s.calibrated)}});
    }

    // The recovery verdict, from the blind best.
    std::optional<double> recovery_distance;
    std::optional<double> blind_iv_rmse;
    bool blind_converged = false;
    if (best_blind.has_value()) {
        const CalibrationStart& blind = result.starts[*best_blind];
        recovery_distance = normalized_parameter_distance(blind.calibrated, truth, bounds);
        blind_iv_rmse = blind.implied_vol_rmse;
        blind_converged = blind.status == NelderMeadStatus::Converged;
    }

    const bool recovery_ok =
        recovery_distance.has_value() && *recovery_distance < config.recovery_tolerance;
    const bool fit_ok = blind_iv_rmse.has_value() && *blind_iv_rmse < config.fit_tolerance_iv_rmse;

    if (!best_blind.has_value() || !blind_converged) {
        // No blind start converged: the run cannot answer the question rather than
        // answering it wrongly.
        record.status = ExperimentStatus::Inconclusive;
    } else if (recovery_ok && fit_ok) {
        record.status = ExperimentStatus::Pass;
    } else if (fit_ok && !recovery_ok) {
        // The surface is fit well by parameters materially away from the truth: a
        // genuine non-identifiability, reported as a warning with the evidence rather
        // than a pass or a silent failure.
        record.status = ExperimentStatus::Warning;
    } else {
        record.status = ExperimentStatus::Fail;
    }

    nlohmann::json best_blind_json = nullptr;
    if (best_blind.has_value()) {
        const CalibrationStart& blind = result.starts[*best_blind];
        best_blind_json = nlohmann::json{
            {"index", *best_blind},
            {"calibrated", to_json(blind.calibrated)},
            {"objective", blind.objective_value},
            {"implied_vol_rmse", blind.implied_vol_rmse},
            {"price_rmse", blind.price_rmse},
            {"status", to_string(blind.status)},
            {"distance_to_truth", *recovery_distance},
            {"parameter_error",
             nlohmann::json{
                 {"initial_variance", blind.calibrated.initial_variance - truth.initial_variance},
                 {"mean_reversion", blind.calibrated.mean_reversion - truth.mean_reversion},
                 {"long_run_variance",
                  blind.calibrated.long_run_variance - truth.long_run_variance},
                 {"vol_of_variance", blind.calibrated.vol_of_variance - truth.vol_of_variance},
                 {"correlation", blind.calibrated.correlation - truth.correlation}}}};
    }

    nlohmann::json similar_fits = nlohmann::json::array();
    for (const HestonParameters& p : result.similar_fits) {
        similar_fits.push_back(to_json(p));
    }

    record.results = nlohmann::json{
        {"starts", std::move(start_records)},
        {"best_blind", std::move(best_blind_json)},
        {"recovery_distance",
         recovery_distance.has_value() ? nlohmann::json(*recovery_distance)
                                       : nlohmann::json(nullptr)},
        {"surface_quote_count", surface.value().quotes.size()},
        {"started_count", started},
        {"converged_count", converged},
        {"convergence_rate",
         started > 0 ? static_cast<double>(converged) / static_cast<double>(started) : 0.0},
        {"non_unique", result.non_unique},
        {"max_similar_fit_distance", result.max_similar_fit_distance},
        {"similar_fits", std::move(similar_fits)},
        {"objective_min", result.objective_min},
        {"objective_mean", result.objective_mean},
        {"objective_max", result.objective_max},
        {"parameter_dispersion", to_json(result.parameter_stddev)},
        {"parameter_mean", to_json(result.parameter_mean)}};

    record.interpretation = fmt::format(
        "Calibration recovers the parameters that generated the surface, and the recovery is read "
        "from a blind start -- the best of the {} guesses that did not begin at the truth -- not "
        "from a start handed the answer. The four things the catalog keeps apart are kept apart "
        "here: the objective value, the surface fit (an implied-volatility RMSE of {} and a price "
        "RMSE of {} at the blind best), the parameter recovery (a normalised distance of {} from "
        "the truth), and identifiability (whether other starts fit about as well from materially "
        "different parameters). {} of {} started fits converged.\n\n"
        "A low objective is never on its own read as success: a fit that matched the surface "
        "closely from the wrong parameters would be reported as a warning about identifiability, "
        "not a pass, and the materially-different fits that a low objective alone would hide are "
        "preserved in similar_fits.",
        blind_count,
        blind_iv_rmse.has_value() ? number(*blind_iv_rmse) : "n/a",
        best_blind.has_value() ? number(result.starts[*best_blind].price_rmse) : "n/a",
        recovery_distance.has_value() ? number(*recovery_distance) : "n/a",
        converged,
        started);

    record.limitations = {
        "Parameter recovery is not uniform across the five parameters. On a surface of European "
        "prices, v0 and theta trade off against each other and kappa against xi, so the "
        "correlation and the variance level are recovered far more tightly than the mean reversion "
        "-- the per-parameter errors in best_blind.parameter_error show which.",
        "Recovery from a synthetic surface bounds the calibration's accuracy under the model's own "
        "assumptions. It says nothing about model error: a real surface is not generated by "
        "Heston, and a good synthetic recovery does not promise a good real-market fit.",
        "The verdict depends on the tolerances chosen (recovery_tolerance, fit_tolerance_iv_rmse) "
        "and on the guesses supplied. A different guess set could land in a different local "
        "minimum; the dispersion across starts and the non-uniqueness flag are the honest measure "
        "of how much that matters here."};

    record.runtime_seconds =
        std::chrono::duration<double>(std::chrono::steady_clock::now() - start_time).count();
    return Result<ExperimentRecord>::success(std::move(record));
}

namespace {

/// One calibrated scenario against the real surface.
struct ScenarioResult {
    std::string name;
    HestonParameters calibrated;
    double objective{};
    double implied_vol_rmse{};
    double price_rmse{};
    NelderMeadStatus status{NelderMeadStatus::MaxIterationsReached};
    bool non_unique{};
    bool relied_on_penalties{};
    int quotes_failed{};
    int quote_count{};
    nlohmann::json residuals;
};

/// A copy of the surface with each quote's weight replaced.
[[nodiscard]] VolatilitySurface
reweighted(const VolatilitySurface& base,
           const std::function<double(const SurfaceQuote&)>& weight) {
    VolatilitySurface s = base;
    for (SurfaceQuote& q : s.quotes) {
        q.weight = weight(q);
    }
    return s;
}

/// A copy of the surface keeping only quotes that satisfy a predicate.
[[nodiscard]] VolatilitySurface subset(const VolatilitySurface& base,
                                       const std::function<bool(const SurfaceQuote&)>& keep) {
    VolatilitySurface s = base;
    std::vector<SurfaceQuote> kept;
    for (const SurfaceQuote& q : base.quotes) {
        if (keep(q)) {
            kept.push_back(q);
        }
    }
    s.quotes = std::move(kept);
    return s;
}

}  // namespace

Result<ExperimentRecord> run_market_surface_stability(const MarketSurfaceStabilityConfig& config) {
    const auto start_time = std::chrono::steady_clock::now();

    ExperimentRecord record;
    record.id = "EXP-12";
    record.name = "Market-Surface Calibration Stability";
    record.question =
        "How stable are calibrated Heston parameters on a fixed documented market surface?";
    record.reproduction_command = "diffusionworks experiment --id EXP-12 --config "
                                  "configs/experiment/calibration_stability.json";

    if (config.initial_guesses.empty()) {
        return Result<ExperimentRecord>::failure(ErrorCode::InvalidArgument,
                                                 "stability study needs at least one initial guess",
                                                 "EXP-12");
    }

    // Load and validate the real dataset. The cleaned surface, its provenance, and the
    // exclusion report are the ground the whole experiment stands on.
    const auto dataset = load_market_dataset(config.dataset_path);
    if (!dataset) {
        return Result<ExperimentRecord>::failure(dataset.error());
    }
    DatasetValidationConfig validation_config;
    validation_config.min_volume = config.min_volume;
    const auto validated = validate_dataset(dataset.value(), validation_config);
    if (!validated) {
        return Result<ExperimentRecord>::failure(validated.error());
    }
    const VolatilitySurface& base = validated.value().surface;
    if (base.quotes.size() < 4) {
        return Result<ExperimentRecord>::failure(
            ErrorCode::InvalidArgument,
            fmt::format(
                "only {} quotes survived validation; too few to study calibration stability",
                base.quotes.size()),
            "EXP-12");
    }

    // The distinct maturities, for the maturity-subset scenario.
    std::vector<double> maturities = validated.value().maturities;
    const double longest_maturity = maturities.empty() ? 0.0 : maturities.back();
    // The strike range, for the core-strike scenario.
    const std::vector<double>& strikes = validated.value().strikes;
    const double lowest_strike = strikes.empty() ? 0.0 : strikes.front();
    const double highest_strike = strikes.empty() ? 0.0 : strikes.back();

    const double spot = base.spot;
    const HestonParameterBounds default_bounds = HestonParameterBounds::defaults();
    HestonParameterBounds tight_bounds;
    tight_bounds.lower = {.initial_variance = 0.005,
                          .mean_reversion = 0.5,
                          .long_run_variance = 0.01,
                          .vol_of_variance = 0.1,
                          .correlation = -0.95};
    tight_bounds.upper = {.initial_variance = 0.08,
                          .mean_reversion = 5.0,
                          .long_run_variance = 0.1,
                          .vol_of_variance = 1.2,
                          .correlation = -0.1};

    const VolatilitySurface atm_weighted = reweighted(base, [&](const SurfaceQuote& q) {
        const double moneyness = q.strike / spot - 1.0;
        return std::exp(-0.5 * (moneyness / 0.04) * (moneyness / 0.04));
    });
    const VolatilitySurface wing_weighted = reweighted(
        base, [&](const SurfaceQuote& q) { return 1.0 + 12.0 * std::abs(q.strike / spot - 1.0); });
    const VolatilitySurface core_strikes = subset(base, [&](const SurfaceQuote& q) {
        return q.strike > lowest_strike && q.strike < highest_strike;
    });
    const VolatilitySurface short_maturities =
        subset(base, [&](const SurfaceQuote& q) { return q.maturity < longest_maturity - 1.0e-9; });

    // The alternative guess set varies the initial conditions relative to the base.
    std::vector<HestonParameters> alt_guesses = config.initial_guesses;
    std::ranges::reverse(alt_guesses);
    for (HestonParameters& g : alt_guesses) {
        g.vol_of_variance = std::min(g.vol_of_variance + 0.2, 1.5);
        g.mean_reversion = std::max(g.mean_reversion - 0.3, 0.2);
    }

    struct Scenario {
        std::string name;
        std::string axis;
        const VolatilitySurface* surface;
        const HestonParameterBounds* bounds;
        const std::vector<HestonParameters>* guesses;
    };

    const std::vector<Scenario> scenarios = {{.name = "base",
                                              .axis = "reference",
                                              .surface = &base,
                                              .bounds = &default_bounds,
                                              .guesses = &config.initial_guesses},
                                             {.name = "alternative_guesses",
                                              .axis = "initial_guesses",
                                              .surface = &base,
                                              .bounds = &default_bounds,
                                              .guesses = &alt_guesses},
                                             {.name = "atm_weighted",
                                              .axis = "weighting",
                                              .surface = &atm_weighted,
                                              .bounds = &default_bounds,
                                              .guesses = &config.initial_guesses},
                                             {.name = "wing_weighted",
                                              .axis = "weighting",
                                              .surface = &wing_weighted,
                                              .bounds = &default_bounds,
                                              .guesses = &config.initial_guesses},
                                             {.name = "tight_bounds",
                                              .axis = "bounds",
                                              .surface = &base,
                                              .bounds = &tight_bounds,
                                              .guesses = &config.initial_guesses},
                                             {.name = "core_strikes",
                                              .axis = "strike_subset",
                                              .surface = &core_strikes,
                                              .bounds = &default_bounds,
                                              .guesses = &config.initial_guesses},
                                             {.name = "short_maturities",
                                              .axis = "maturity_subset",
                                              .surface = &short_maturities,
                                              .bounds = &default_bounds,
                                              .guesses = &config.initial_guesses}};

    record.table.headers = {"scenario",
                            "axis",
                            "status",
                            "iv_rmse",
                            "price_rmse",
                            "v0",
                            "kappa",
                            "theta",
                            "xi",
                            "rho",
                            "penalized",
                            "non_unique"};

    std::vector<ScenarioResult> results;
    results.reserve(scenarios.size());
    for (const Scenario& scenario : scenarios) {
        CalibrationConfig calibration;
        calibration.bounds = *scenario.bounds;
        calibration.initial_guesses = *scenario.guesses;
        calibration.objective = config.objective;
        calibration.pricing = HestonAnalyticConfig{.quadrature_nodes = config.quadrature_nodes,
                                                   .convergence_tolerance = 1.0e-5};
        calibration.optimizer.max_iterations = config.max_iterations;

        const auto calibrated = calibrate_heston(*scenario.surface, calibration);
        if (!calibrated) {
            return Result<ExperimentRecord>::failure(calibrated.error());
        }
        const CalibrationStart& best = calibrated.value().best;

        ScenarioResult r;
        r.name = scenario.name;
        r.calibrated = best.calibrated;
        r.objective = best.objective_value;
        r.implied_vol_rmse = best.implied_vol_rmse;
        r.price_rmse = best.price_rmse;
        r.status = best.status;
        r.non_unique = calibrated.value().non_unique;
        r.relied_on_penalties = calibrated.value().relied_on_penalties;
        r.quotes_failed = best.quotes_failed;
        r.quote_count = static_cast<int>(scenario.surface->quotes.size());
        r.residuals = nlohmann::json::array();
        for (const QuoteResidual& q : calibrated.value().best_residuals) {
            r.residuals.push_back(nlohmann::json{
                {"option_type", to_string(q.type)},
                {"strike", q.strike},
                {"maturity", q.maturity},
                {"weight", q.weight},
                {"market_implied_volatility", q.market_implied_volatility},
                {"model_implied_volatility", q.model_implied_volatility},
                {"iv_residual", q.model_implied_volatility - q.market_implied_volatility}});
        }
        results.push_back(std::move(r));
    }

    // Cross-scenario parameter dispersion: how much the calibrated parameters move as
    // the setup varies. This spread, not any single fit, is the answer.
    const auto spread = [&](const std::function<double(const HestonParameters&)>& get) {
        double sum = 0.0;
        double min_v = get(results.front().calibrated);
        double max_v = min_v;
        for (const ScenarioResult& r : results) {
            const double v = get(r.calibrated);
            sum += v;
            min_v = std::min(min_v, v);
            max_v = std::max(max_v, v);
        }
        const double mean = sum / static_cast<double>(results.size());
        double var = 0.0;
        for (const ScenarioResult& r : results) {
            const double d = get(r.calibrated) - mean;
            var += d * d;
        }
        const double stddev =
            results.size() >= 2 ? std::sqrt(var / static_cast<double>(results.size() - 1)) : 0.0;
        return nlohmann::json{{"mean", mean}, {"stddev", stddev}, {"min", min_v}, {"max", max_v}};
    };
    nlohmann::json dispersion{
        {"initial_variance", spread([](const HestonParameters& p) { return p.initial_variance; })},
        {"mean_reversion", spread([](const HestonParameters& p) { return p.mean_reversion; })},
        {"long_run_variance",
         spread([](const HestonParameters& p) { return p.long_run_variance; })},
        {"vol_of_variance", spread([](const HestonParameters& p) { return p.vol_of_variance; })},
        {"correlation", spread([](const HestonParameters& p) { return p.correlation; })}};

    // Identifiability. A parameter whose spread across the scenarios is comparable to
    // its own magnitude is not determined by this surface: the scenarios all fit, and
    // still disagree about what the parameter is. That is a property of the data and
    // the model, not of the optimiser, so it is measured here rather than inferred
    // from a converged flag.
    constexpr double kDispersionWarning = 0.50;
    double max_relative_dispersion = 0.0;
    std::string least_identified_parameter;
    for (const auto& [name, stats] : dispersion.items()) {
        const double mean = stats.at("mean").get<double>();
        if (mean == 0.0) {
            continue;
        }
        const double relative = stats.at("stddev").get<double>() / std::abs(mean);
        if (relative > max_relative_dispersion) {
            max_relative_dispersion = relative;
            least_identified_parameter = name;
        }
    }
    const bool parameters_not_identified = max_relative_dispersion > kDispersionWarning;
    if (parameters_not_identified) {
        record.limitations.push_back(fmt::format(
            "The scenarios all converged and all fit the surface, and still disagree about the "
            "parameters: {} varies by {:.0f}% of its own mean across them, above the {:.0f}% "
            "threshold at which a parameter is treated as not identified by this surface. The "
            "fitted values are therefore usable for repricing the quotes they were fitted to, "
            "and are not a measurement of the parameter itself.",
            least_identified_parameter,
            max_relative_dispersion * 100.0,
            kDispersionWarning * 100.0));
    }

    // The exclusion report from validation.
    nlohmann::json excluded = nlohmann::json::array();
    for (const ExcludedQuote& e : validated.value().excluded) {
        excluded.push_back(nlohmann::json{{"option_type", to_string(e.quote.type)},
                                          {"strike", e.quote.strike},
                                          {"maturity", e.quote.maturity_years},
                                          {"price", e.quote.price},
                                          {"volume", e.quote.volume},
                                          {"reason", to_string(e.reason)},
                                          {"detail", e.detail}});
    }

    nlohmann::json scenario_json = nlohmann::json::array();
    bool all_converged = true;
    bool any_relied_on_penalties = false;
    for (const ScenarioResult& r : results) {
        all_converged = all_converged && r.status == NelderMeadStatus::Converged;
        any_relied_on_penalties = any_relied_on_penalties || r.relied_on_penalties;
        record.table.rows.push_back({r.name,
                                     "",  // axis filled from scenarios below via json only
                                     std::string(to_string(r.status)),
                                     number(r.implied_vol_rmse),
                                     number(r.price_rmse),
                                     number(r.calibrated.initial_variance),
                                     number(r.calibrated.mean_reversion),
                                     number(r.calibrated.long_run_variance),
                                     number(r.calibrated.vol_of_variance),
                                     number(r.calibrated.correlation),
                                     std::to_string(r.quotes_failed),
                                     r.non_unique ? "yes" : "no"});
        scenario_json.push_back(nlohmann::json{{"scenario", r.name},
                                               {"status", to_string(r.status)},
                                               {"objective", r.objective},
                                               {"implied_vol_rmse", r.implied_vol_rmse},
                                               {"price_rmse", r.price_rmse},
                                               {"quote_count", r.quote_count},
                                               {"penalized_quote_count", r.quotes_failed},
                                               {"relied_on_penalties", r.relied_on_penalties},
                                               {"non_unique", r.non_unique},
                                               {"calibrated", to_json(r.calibrated)},
                                               {"residual_surface", r.residuals}});
    }
    // Fill the axis column now that the scenarios and rows align by index.
    for (std::size_t i = 0; i < scenarios.size() && i < record.table.rows.size(); ++i) {
        record.table.rows[i][1] = scenarios[i].axis;
    }

    record.configuration = nlohmann::json{{"dataset_path", config.dataset_path},
                                          {"objective", to_string(config.objective)},
                                          {"quadrature_nodes", config.quadrature_nodes},
                                          {"max_iterations", config.max_iterations},
                                          {"min_volume", config.min_volume},
                                          {"initial_guess_count", config.initial_guesses.size()}};

    record.results = nlohmann::json{
        {"dataset",
         nlohmann::json{{"underlying", dataset.value().metadata.underlying},
                        {"observation_timestamp", dataset.value().metadata.observation_timestamp},
                        {"spot", dataset.value().metadata.spot},
                        {"rate", dataset.value().metadata.rate},
                        {"dividend_yield", dataset.value().metadata.dividend_yield},
                        {"source", dataset.value().metadata.source},
                        {"licensing", dataset.value().metadata.licensing}}},
        {"total_quotes", validated.value().total_quotes},
        {"included_quotes", validated.value().included_quotes},
        {"excluded_quote_count", validated.value().excluded.size()},
        {"exclusions_by_reason",
         nlohmann::json{{"low_volume", validated.value().low_volume},
                        {"non_positive_price", validated.value().non_positive_price},
                        {"arbitrage_violation", validated.value().arbitrage_violations},
                        {"no_implied_volatility", validated.value().inversion_failures},
                        {"implied_volatility_out_of_band", validated.value().out_of_band}}},
        {"excluded_quotes", std::move(excluded)},
        {"maturity_count", validated.value().maturities.size()},
        {"strike_count", validated.value().strikes.size()},
        {"scenarios", std::move(scenario_json)},
        {"parameter_dispersion", std::move(dispersion)},
        {"max_relative_parameter_dispersion", max_relative_dispersion},
        {"least_identified_parameter", least_identified_parameter},
        {"parameter_dispersion_warning_threshold", kDispersionWarning},
        {"parameters_not_identified", parameters_not_identified},
        {"any_scenario_relied_on_penalties", any_relied_on_penalties}};

    // Status. It describes the empirical result, not whether the solver ran. A fit
    // that leaned on penalties or failed to converge is not a clean pass; neither is
    // a set of scenarios that all fit well and still cannot agree on the parameters,
    // which is the finding this experiment exists to expose. Reporting that as a pass
    // because every scenario converged would report the solver's health in place of
    // the answer.
    if (any_relied_on_penalties || !all_converged || parameters_not_identified) {
        record.status = ExperimentStatus::Warning;
    } else {
        record.status = ExperimentStatus::Pass;
    }

    record.interpretation =
        "The calibrated parameters are read off a fixed real option surface (SPY, documented "
        "provenance and timestamp) under variations of all four axes the catalog names -- the "
        "initial guesses, the weighting of the quotes, the parameter bounds, and the strike and "
        "maturity subset -- and the spread of the results across those variations, in "
        "parameter_dispersion, is the finding. Unlike the synthetic surface used for controlled "
        "recovery, this one is not generated by Heston, so the fit carries genuine model error: "
        "the per-scenario implied-volatility RMSE is a real residual, and the residual_surface of "
        "each scenario shows where Heston tracks the smile and where it cannot.\n\n"
        "Convergence, fit RMSE, residual structure, the penalized-quote count, and parameter "
        "dispersion are reported separately, and the status is driven by all of them rather than "
        "by convergence alone. A fit that leaned on penalties is kept out of clean-pass status, "
        "and so is a set of scenarios that all converge and all fit while disagreeing about the "
        "parameters by more than half their own magnitude: that disagreement is the answer to the "
        "question this experiment asks, so reporting it as a pass because the solver succeeded "
        "would report the wrong thing. The dataset's excluded quotes are reported by reason, so "
        "the surface the calibration ran on is exactly the surface a reader can audit.";

    record.limitations = {
        "The dataset is a small end-of-day snapshot of last-trade closing prices, not a "
        "synchronous "
        "bid/ask quote set; closes across strikes are not perfectly simultaneous, which adds noise "
        "the residual RMSE partly reflects.",
        "SPY options are American; only out-of-the-money options are used and treated as European, "
        "a small but non-zero approximation documented in the dataset.",
        "Stability across setups is not identifiability. Parameters can co-move in a way that "
        "keeps "
        "the fit good, so a small per-scenario RMSE with a large dispersion is the weak-"
        "identification signal this experiment exists to surface; the two are reported separately.",
        "The scenarios are a fixed menu across the four axes; a wider set could expose instability "
        "this one does not, so a small dispersion bounds instability only over the variations "
        "tried."};

    record.runtime_seconds =
        std::chrono::duration<double>(std::chrono::steady_clock::now() - start_time).count();
    return Result<ExperimentRecord>::success(std::move(record));
}

